/*
                |Nombre        |   Sintaxis|
                |--------------|-----------|
                |AND           | &&        |
                |OR            | ||        |
                |NOT           | !         |
                
*/

console.info("AND")
console.log(3 > 2 && 2 > 1)

console.info("OR")
console.log(3 > 2 || 2 > 2)//true
console.log(2 > 2 || 2 > 2)//false

console.info("NOT")
console.log(!true)//false
console.log(!false)//true
